# ── Exit code tests via subprocess ────────────────────────────────────────────

def test_checker_exits_nonzero_on_dirty(tmp_path):
    root = "HAL-YieldOS-v3.0.11-poc-release"
    z = _make_clean_zip(tmp_path, root)
    with zipfile.ZipFile(z, "a") as zf:
        zf.writestr(f"{root}/.ruff_cache/v0/CACHEDIR.TAG", "dirty")
    result = subprocess.run(
        [sys.executable, str(CHECKER_SCRIPT), str(z)],
        capture_output=True, text=True, timeout=15,
    )
    assert result.returncode != 0, \
        f"checker must exit non-zero on dirty zip, got {result.returncode}"
    assert "FAIL" in result.stdout, \
        f"checker must print FAIL on dirty zip\nstdout: {result.stdout}"


@pytest.mark.release_heavy
def test_checker_exits_zero_on_clean_generated_release_zip():
    """Checker exits 0 on the official zip generated by build_release.py."""
    zip_path = ROOT / "dist" / f"HAL-YieldOS-v{VERSION}-poc-release.zip"
    if not zip_path.exists():
        pytest.skip("Release zip not found -- run build_release.py first")
    result = subprocess.run(
        [sys.executable, str(CHECKER_SCRIPT), str(zip_path)],
        capture_output=True, text=True, timeout=15,
    )
    assert result.returncode == 0, (
        f"checker must exit 0 on official zip\n"
        f"stdout: {result.stdout}\nstderr: {result.stderr}"
    )
    assert "PASS" in result.stdout


