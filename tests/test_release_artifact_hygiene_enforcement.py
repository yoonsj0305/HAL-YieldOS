"""Tests for v3.0.11 -- release artifact hygiene enforcement and dirty archive prevention."""
from __future__ import annotations

import importlib.util
import subprocess
import sys
import zipfile
from pathlib import Path

import pytest

ROOT = Path(__file__).parent.parent
CHECKER_SCRIPT = ROOT / "scripts" / "check_release_artifact.py"
WORKFLOWS = ROOT / ".github" / "workflows"
VERSION = (ROOT / "VERSION").read_text(encoding="utf-8-sig").strip()


def _load_checker():
    spec = importlib.util.spec_from_file_location("check_release_artifact", CHECKER_SCRIPT)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def _make_clean_zip(tmp_path: Path, root_name: str = "HAL-YieldOS-v3.0.11-poc-release") -> Path:
    """Create a minimal clean release zip that passes hygiene checks."""
    z = tmp_path / f"{root_name}.zip"
    required = [
        "MANIFEST.json",
        "VERSION",
        "pyproject.toml",
        "README.md",
        "RELEASE_NOTES.md",
        "scripts/check_launch_guard.py",
        "scripts/run_public_demo.py",
        "docs/PUBLIC_SAFETY_BOUNDARY.md",
        ".github/workflows/tests.yml",
        ".github/workflows/public-demo.yml",
    ]
    with zipfile.ZipFile(z, "w") as zf:
        for entry in required:
            zf.writestr(f"{root_name}/{entry}", "placeholder")
    return z


# ── Script existence ──────────────────────────────────────────────────────────

def test_check_release_artifact_script_exists():
    assert CHECKER_SCRIPT.exists(), "scripts/check_release_artifact.py must exist"


# ── Official zip validation (skips if zip not yet built) ─────────────────────

@pytest.mark.packaging
def test_validate_official_zip_passes_after_build():
    """Official zip generated by build_release.py must pass hygiene check."""
    zip_path = ROOT / "dist" / f"HAL-YieldOS-v{VERSION}-poc-release.zip"
    if not zip_path.exists():
        pytest.skip("Release zip not found -- run build_release.py first")
    mod = _load_checker()
    errors = mod.validate_release_artifact(zip_path, expected_version=VERSION)
    assert errors == [], f"Official zip failed hygiene: {errors}"


